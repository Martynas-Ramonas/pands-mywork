# Get the number from the user
number = float(input('Enter a number: '))

# Use the abs() function to get the absolute value of a number.
# The abs() function returns the absolute value of a number, which is the distance of the number from zero on the number line. 
# For example, the absolute value of -5 is 5, and the absolute value of 5 is also 5.
absolute_value = abs(number)

# Print the absolute value
print(f'The absolute value of {number} is {absolute_value}')